# Todos os três métodos servem para imprimir na tela uma mensagem.
# Podem ser usados com parêntese ou não.

# O puts abaixo imprime quebrando a linha, será apresenta em colunas cada string
puts ['A', 'B', 'C']

# O p e o Print imprime as letras uam ao lado do outa
p ['A', 'B', 'C']
print ['A', 'B', 'C']

=begin
  O p faz a inspeção no código, ou seja, apresenta coisas ocultas no código.
=end
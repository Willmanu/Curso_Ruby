=begin
                                              Criando um projeto
Rails é um Gem, ou seja, uma biblioteca
Sua instalação pode ser feita pelo terminal com o comando:

gems installed rails

Após esse comando, o Rails baixa todas as dependências, as instala e instala também a biblioteca
Após essa instalação é possível criar um projeto
Indo no terminal e escrevendo:
rails new nome_projeto, dando enter nesta linha, o rails cria as estruturas de diretórios dessa
aplicação
Cria-se um diretório com varias pastas e em uma delas, a bin, entrando nela é possível
inicializar o servidor com o comando:
bin/rails server

bin é uns dos diretórios, como dito acima, que o rails criou
rails é um dos scripts
 server é o comando para servir a aplicação
 Após rodar esse comando o rails inicializa o servidor web e mostra a porta que esta escutando,
 o caminho etc.
 Com esse caminho escrito no browser, será possível ver essa nova aplicação

=end

                                       POO
POO é programação orientada a objetos.

Um objeto segundo livros de programação é:
Coisa material ou abstrata que pode ser percebida pelos sentidos e descrita por meio das suas
características, comportamento e estado atual.
Coisas materiais é fácil saber: um celular, um carro, uma caneta, mouse etc.
E as abstratas como por exemplo: um compromisso agendado? não podemos tocar no compromisso,
logo não é uma coisa material e sim é um objeto abstrato.

                                       Objeto abstrato

Para saber se algo se enquadra em como objeto em programação temos que observar algumas
coisas que são:
- Características
- Comportamento
- Estado atual
A exemplo veremos se Compromisso é um objeto.
Características:
mês, dia, hora em que foi marcado, o tipo de roupa

Comportamento:
Pode ser marcado, desmarcado, adiado, cancelado etc.

Estado:
Esta combinado, estado de agora foi suspenso, remarcado para um horário
mais cedo ou mais tarde etc.

Levantando esses conceitos, e atendo todos os requisitos, podemos dizer que Compromisso é
um objeto.

                                Objeto material
Exemplo caneta:

Características:
tamanho, cor, ponta fina ou grossa etc..

Comportamento:
Pode escrever, rabiscar, sublinhar, pintar etc.

Estado:
Nova, usada, com tinta, sem tinta, tampada ou destampada etc.

                           Classe

Classe é molde, ou seja, o modelo no qual vários objetos vão ser construído a partir desse
modelo ou o esquema ou a planta de uma construção.
Imagine construir varias casas populares. Para isso todas as casas vão ser construídas com
uma única planta.
Com relação a caneta teremos o mesmo molde para fazer varias de cores diferentes
A caneta pronta é o objeto.


Para construir uma classe precisamos responder 3 perguntas:

1º Que coisas eu tenho?
2º Que coisas eu faço?
3º Como estou agora?

Que coisas eu tenho
Exemplo: Modelo, Cor, ponta, carga, tampa

Que coisas eu faço
Exemplo: Escrever, Rabiscar, Sublinhar, pintar, tampar, Destampar etc.

Como estou agora
Exemplo: Nova com 100% de carga tampada etc.
ou velha, com 10% de carga sem tampa etc.




O criador de POO foi Alan Kay, um matemático e biólogo.
Ele é apaixonado por educação e principalmente a educação de crianças. Com o conhecimento
lógico que conquistou com a matemática e o conhecimento de biologia formou os grandes
conceitos da POO.
Este formulou o seguinte postulado: ano de 1970 nem existia graduação em informática
"O computador ideal deve funcionar como um organismo vivo, isso é: cada célula se relaciona
com outras a fim de alcançar um objetivo, mas cada uma funciona de forma autônoma.
As células poderiam se agrupar para resolver um outro problema ou desempenhar outras funções."

Perceba nesta palavras que são carregadas de conhecimentos em biologia, matemática e raciocínio
lógico.

A beleza de POO é que fica simples pensar em uma aplicação pensando no objeto que ela se
dispõe a ser.

Assim qualquer coisa que seja considerado um objeto, pode se aplicar os conceitos criados
por Alan Kay para POO.

                                           Antes de POO
Antes de poo tínhamos:
Década de 40- 50 quando os computadores começaram a surgir.

1º Programação de baixo nível - cada computar tinhas suas instruções de acordo com suas
características. Se o pc era em decimal as instruções eram em decimal, se em binário etc.
E só os engenheiros que faziam a programação.

2º Programação Linear

3º Programação Estruturada

4º Programação modular

5º Poo

                              Forma de acessar os dados antes de POO

Na programação estruturada e programação modular o conjunto de dados são tratados todos juntos.
Temos uma grande massa de dados que são tratadas por procedimentos.
Os procedimentos/métodos/funções, tem acesso a essa massa dados, processam e geram resultados.

O problema é que nem todos os procedimentos vão fazer uso de todos os dados necessários.
Só farão uso dos dados que lhe convêm.
Imaginem uma sala cheia de gente e cada um precisa ser atendido por uma coisa específica.
Antes de começar a funcionar os procedimentos precisam filtrar os dados para saber quais são
uteis.


                                     Proposta de POO
A proposta de poo e criar as coisas um pouco menor e assim são criados os objetos.
Aqui agora temos os dados de Objetos.
Dessa forma os procedimentos/métodos/funções só trabalhariam os dados daquele objeto sem criar
os filtros adicionais.
Assim é possível ter vários objetos e trocar dados entre os objetos.


                             Exemplo programar um controle remoto

Imagina programar um controle remoto sem POO
Sem poo a preocupação seria pensar nos detalhes, muitos procedimentos etc.
O foco seria nos circuitos internos e seus detalhes e como se inter-relacionam.

Em POO o foco seria no objeto controle remoto.
Focar nas funcionalidades que cada botão tem, em cada opção tem e nas ações que eles
vão executar caso sejam pressionados.
Baseado neste raciocínio programar em poo é mais simples.

